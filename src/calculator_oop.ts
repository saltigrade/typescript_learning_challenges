export class Calculator {
  add(a: number, b: number) {
    const result = a + b;
    return result;
  }

  subtract(a: number, b: number) {
    const result = a - b;
    return result;
  }

  multiply(a: number, b: number) {
    const result = a * b;
    return result;
  }

  divide(a: number, b: number) {
    const result = a / b;
    return result;
  }
}
